package com.test.mathclassexample;

public class MathClassTest {

	public static void main(String[] args) {
		int a = 4, b = 10;
		System.out.println("Largest number::" + Math.max(a, b));
		System.out.println("smallest number::" + Math.min(a, b));

		System.out.println("square root number::" + Math.sqrt(a));
		System.out.println("square of number::" + Math.pow(4, 2));

		System.out.println("cube root number::" + Math.cbrt(8));
		System.out.println("ceil of  number::" + Math.ceil(4.8));

		System.out.println("floor of number::" + Math.floor(4.7));

		System.out.println("sine value :" + Math.sin(3.144 / 6));
		System.out.println("log value::" + Math.log(100));

	}

}
